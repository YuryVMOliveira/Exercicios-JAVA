    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         try {
            // Valida√ß√£o dos campos obrigat√≥rios
            if (txtNome.getText().trim().isEmpty() ||
                    txtCPF.getText().trim().isEmpty() ||
                    txtDataNasc.getText().trim().isEmpty()
                    ) {

                JOptionPane.showMessageDialog(this, "Todos os campos s√£o obrigat√≥rios!", "Erro", JOptionPane.ERROR_MESSAGE);
                return; // Interrompe o cadastro se houver campos vazios
            }
        
        String nome = txtNome.getText();
        String data = txtDataNasc.getText();
        String cpfString = txtCPF.getText();

        if(!pessoaList.isEmpty()){
            for (Pessoa pessoa : pessoaList) {
                if(pessoa.getCpf().toString().equals(cpfString)){
                    JOptionPane.showMessageDialog(this, "CPF j√° cadastrado!", "Erro", JOptionPane.ERROR_MESSAGE);
                    return; // Interrompe o cadastro se o CPF j√° existir
                }
            }
        }
        CPF cpf=CPF.parser(cpfString);
        DataNascimento dataNascimento=DataNascimento.parser(data);
        Pessoa pessoa = new Pessoa(nome,dataNascimento,cpf);
        pessoaList.add(pessoa);

        atualizarTabela();
         }
         catch (Exception e){
             JOptionPane.showMessageDialog(this, "Erro ao adicionar a pessoa!\n Formato cpf: xxx.xxx.xxx-xx\n Data: xx/xx/xxxx", "Erro", JOptionPane.ERROR_MESSAGE);
         }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Selecione uma pessoa para remover.", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Remove a pessoa da lista
    pessoaList.remove(selectedRow);

    // Atualiza a tabela
    atualizarTabela();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Selecione uma pessoa para editar.", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    try {
        // Obt√©m os valores dos campos
        String nome = txtNome.getText();
        String data = txtDataNasc.getText();
        String cpfString = txtCPF.getText();

        // Verifica se o CPF j√° est√° sendo usado por outra pessoa
        for (int i = 0; i < pessoaList.size(); i++) {
            if (i != selectedRow && pessoaList.get(i).getCpf().toString().equals(cpfString)) {
                JOptionPane.showMessageDialog(this, "CPF j√° cadastrado por outra pessoa!", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        // Atualiza a pessoa na lista
        Pessoa pessoa = pessoaList.get(selectedRow);
        
        pessoa.setNome(nome);
        pessoa.setDataNascimento(DataNascimento.parser(data));
        pessoa.setCpf(CPF.parser(cpfString));
        pessoaList.remove(pessoaList.get(selectedRow));
        pessoaList.add(pessoa);
        // Atualiza a tabela
        atualizarTabela();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao editar a pessoa!", "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton3ActionPerformed

