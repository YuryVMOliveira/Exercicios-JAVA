    private void atualizarTabela() {
        // Limpa a tabela
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                new Object [][] {},
                new String [] {"Nome", "Data de Nascimento", "Idade", "CPF"}
        ));

        // Adiciona os dados da lista √† tabela
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        for (Pessoa pessoa : pessoaList) {
            Object[] rowData = {pessoa.getNome(), pessoa.getDataNascimento().toString(), pessoa.getIdade(), pessoa.getCpf().toString()};
            model.addRow(rowData);
        }
    }

